/**
 * Class that represents the day of a week.
 */
public enum DayOfWeek {
  /**
   * Represents Sunday
   */
  SUN,
  /**
   * Represents Monday
   */
  MON,
  /**
   * Represents Tuesday
   */
  TUE,
  /**
   * Represents Wednesday
   */
  WED,
  /**
   * Represents Thursday
   */
  THU,
  /**
   * Represents Friday
   */
  FRI,
  /**
   * Represents Saturday
   */
  SAT
}
